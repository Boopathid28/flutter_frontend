class MetalService {

  static Future fetchMetalList({
    FetchMetalListPayload? payload,
  }) async {
    var response = await ApiCalls.postWithToken(
        url: metalMasterListEndPoint, body: payload!.toJson());

    if (response != null) {
      if (response['status'] == 200) {
        List<MetalListData> tempList = <MetalListData>[];
        for (var item in response['data']['list']) {
          tempList.add(MetalListData.fromJson(item));
        }
        return {
          "data": tempList,
          "total_pages": response['data']['total_pages']
        };
      } else if (response['status'] == 401) {
        navLogin();
        return null;
      }
    }

    return null;
  }

  static Future createMetal(
      {CreateMetalPayload? payload, required BuildContext context}) async {
    var response = await ApiCalls.postWithToken(
        url: metalMasterEndPoint, body: payload!.toJson());

    if (response != null) {      

      if (response['status'] == 200) {
        final MetalCreateResponse metalCreateResponse =
          MetalCreateResponse.fromJson(response['data']);
        toastification.show(
          context: context, // optional if you use ToastificationWrapper
          type: ToastificationType.success,
          title: Text(response['message']),
          autoCloseDuration: notificationDuration,
          showProgressBar: false,
          pauseOnHover: false
        );
        return metalCreateResponse;
      } else if (response['status'] == 400) {

        response['data'].forEach((final String key, final value) {
          toastification.show(
            context: context, // optional if you use ToastificationWrapper
            type: ToastificationType.error,
            title: Text(value[0].toString()),
            autoCloseDuration: notificationDuration,
            showProgressBar: false,
            pauseOnHover: false
          );
        });
        return null;
      } else if (response['status'] == 401) {
        navLogin();
      }
      
      
    }

    toastification.show(
          context: context, // optional if you use ToastificationWrapper
          type: ToastificationType.error,
          title: Text(response['message']),
          autoCloseDuration: notificationDuration,
          showProgressBar: false,
          pauseOnHover: false
        );

    return null;
  }

