class SuspenseService {

  static Future fetchSuspense({
    FetchSuspenseListPayload? payload,
  }) async {
    var response = await ApiCalls.postWithToken(
        url: suspenseListEndPoint, body: payload!.toJson());
    if (response != null) {
      if (response['status'] == 200) {
        List<SuspenseListData> tempList = <SuspenseListData>[];
        for (var item in response['data']['list']) {
          tempList.add(SuspenseListData.fromJson(item));
        }
        return {
          "data": tempList,
          "total_pages": response['data']['total_pages']
        };
      } else if (response['status'] == 401) {
        navLogin();
        return null;
      }
    }

    return null;
  }

  static Future createSuspense(
      {CreateSuspensePayload? payload, required BuildContext context}) async {
    var response = await ApiCalls.postWithToken(
        url: suspenseEndPoint, body: payload!.toJson());

    if (response != null) {      

      if (response['status'] == 200) {
        final CreateSuspenseResponse createSuspenseResponse =
          CreateSuspenseResponse.fromJson(response['data']);
        toastification.show(
          context: context, // optional if you use ToastificationWrapper
          type: ToastificationType.success,
          title: Text(response['message']),
          autoCloseDuration: notificationDuration,
          showProgressBar: false,
          pauseOnHover: false
        );
        return createSuspenseResponse;
      } else if (response['status'] == 400) {

        response['data'].forEach((final String key, final value) {
          toastification.show(
            context: context, // optional if you use ToastificationWrapper
            type: ToastificationType.error,
            title: Text(value[0].toString()),
            autoCloseDuration: notificationDuration,
            showProgressBar: false,
            pauseOnHover: false
          );
        });
        return null;
      } else if (response['status'] == 401) {
        navLogin();
      }
      
      
    }

    toastification.show(
          context: context, // optional if you use ToastificationWrapper
          type: ToastificationType.error,
          title: Text(response['message']),
          autoCloseDuration: notificationDuration,
          showProgressBar: false,
          pauseOnHover: false
        );

    return null;
  }


